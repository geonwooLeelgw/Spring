package bean;

public class AdvertisementBean {

	int advertisement_num;
	String company_name;
	int company_tel;
	String company_address;
	
	
	public int getAdvertisement_num() {
		return advertisement_num;
	}
	public void setAdvertisement_num(int advertisement_num) {
		this.advertisement_num = advertisement_num;
	}
	public String getCompany_name() {
		return company_name;
	}
	public void setCompany_name(String company_name) {
		this.company_name = company_name;
	}
	public int getCompany_tel() {
		return company_tel;
	}
	public void setCompany_tel(int company_tel) {
		this.company_tel = company_tel;
	}
	public String getCompany_address() {
		return company_address;
	}
	public void setCompany_address(String company_address) {
		this.company_address = company_address;
	}
	
	
	
}
