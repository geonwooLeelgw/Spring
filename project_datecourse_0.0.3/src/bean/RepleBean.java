package bean;

public class RepleBean {
	private int course_num;
	private int reple_num;
	private String reple_content;
	private String id;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public int getCourse_num() {
		return course_num;
	}
	public void setCourse_num(int course_num) {
		this.course_num = course_num;
	}
	public int getReple_num() {
		return reple_num;
	}
	public void setReple_num(int reple_num) {
		this.reple_num = reple_num;
	}
	public String getReple_content() {
		return reple_content;
	}
	public void setReple_content(String reple_content) {
		this.reple_content = reple_content;
	}
	
	
}
